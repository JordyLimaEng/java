import java.util.Locale;
import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		
		//bitwise
			//& AND bit a bit
			//| OR bit a bit
			//^ XOR bit a bit
		int n1 = 89, n2 = 60;

		System.out.println(n1 & n2);//0101 1001 AND 0011 1100
		System.out.println(n1 | n2);//0101 1001 OR  0011 1100
		System.out.println(n1 ^ n2);//0101 1001 XOR 0011 1100					
	}

}
